package hosh.runtime;

import hosh.doc.Todo;
import hosh.spi.Errors;
import hosh.spi.ExitStatus;
import hosh.spi.InputChannel;
import hosh.spi.Key;
import hosh.spi.Keys;
import hosh.spi.OutputChannel;
import hosh.spi.Record;
import hosh.spi.State;
import hosh.spi.StateAware;
import hosh.spi.StateMutator;
import hosh.spi.StateMutatorAware;
import hosh.spi.Value;
import hosh.spi.VariableName;

import java.util.HashMap;
import java.util.List;
import java.util.Optional;

// generated by compiler for 'cmd1 | { key -> cmd2 ${key} }'
class LambdaCommand implements CompilerCommand, InterpreterAware, StateAware, StateMutatorAware {

	private final Compiler.Statement statement;
	private final String key;
	private Interpreter interpreter;
	private State state;
	private StateMutator stateMutator;

	public LambdaCommand(Compiler.Statement statement, String key) {
		this.statement = statement;
		this.key = key;
	}

	@Override
	public void setInterpreter(Interpreter interpreter) {
		this.interpreter = interpreter;
	}

	@Override
	public void setState(State state) {
		this.state = state;
	}

	@Override
	public void setStateMutator(StateMutator stateMutator) {
		this.stateMutator = stateMutator;
	}

	@Todo(description = "generalize with a list of keys")
	@Override
	public ExitStatus run(List<String> args, InputChannel in, OutputChannel out, OutputChannel err) {
		// at this point key is both a valid variable name and key (key is anyway a superset)
		Key lambdaParameterKey = Keys.of(key);
		VariableName variableName = VariableName.constant(key);
		for (Record record : InputChannel.iterate(in)) {
			Optional<Value> lambdaParameter = record.value(lambdaParameterKey);
			if (lambdaParameter.isEmpty()) {
				err.send(Errors.message("missing key '%s'", key));
				return ExitStatus.error();
			}
			Value value = lambdaParameter.orElseThrow(IllegalArgumentException::new);
			var original = state.getVariables();
			var modified = new HashMap<>(original);
			modified.put(variableName, value.unwrap(String.class).orElse("unwrap failed"));
			stateMutator.mutateVariables(modified); // beware: global state is mutated here, this change should be local
			try {
				ExitStatus eval = interpreter.eval(statement, in, out, err);
				if (eval.isError()) {
					return eval;
				}
			} finally {
				// any change made by inner statement to variable will be lost here
				stateMutator.mutateVariables(original);
			}
		}
		return ExitStatus.success();
	}

	public String getKey() {
		return key;
	}
}
